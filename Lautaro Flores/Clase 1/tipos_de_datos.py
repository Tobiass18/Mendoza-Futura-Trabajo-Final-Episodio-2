# vamos a utilizar función Type para determinar el tipo de dato

#Dato Tipo String -> es un  conjunto de caracteres
print(type("Hola"))
print("Hola")
#Dato tipo INTERGER -> Es un numero entero(numero redonditp)
print(type(10))
print(10)
#¿Que pasa si pongo un numero entero entre comillas""
print(type("10"))
print("10")
#Dato tipo FLOAT -> Numero con coma(decimal) utilizar puntos en ve de comas
print(type(10.9))
print(10.9)
