a = float(input("ingresa otro valor"))
b = float(input("ingresar otro valor"))

# Suma
c = a + b
print(f"La suma da como resultado: {c}")

# Resta "-"
c = a - b
print(f"La resta da como resultado: {c}")

# Multiplicacion "*"
c = a * b
print(f"La multiplicacion da como resultado: {c}")

# Division "/"
c = a / b
print(f"La division da como resultado: {c}")

# Potencia "**"
c = a ** b
print(f"La Potencia da como resultado: {c}")

# Division Entera  "//" # Lo que hace es dividir y te rodondea a un numero entero para abajo
c = a // b
print(f"La Division Entera da como resultado: {c}")

# Modulo    "%" # Lo que hace este es dividir y solo te da numeros decimales, seria el resto y el consiente
c = a % b
print(f"La Modulo % da como resultado: {c}")
