# VARIABLES: es un nombre que almacena en su memoria un dato 
# Las Variables se escriben en formato snake_case 
# Palabras en mininusculas separadas con _
# VARIABLE TIPO INT 
a = 3 
print(a)
b = 2 
print(b)
# conector / concatenar 
# int + int = int 
# 2 + 3 = 5
c = a + b 
print(c)
# VARIABLE TIPO STRING
nombre ="Gabriel"
print(nombre)
apellido = "Zamora"
print(apellido)
# concatear 
# STRIN + STRING = STRING 
# Hola + Mundo  = Hola Mundo 
nombre_completo = nombre + apellido 
print(nombre_completo)
# Agregamos espacio
# opcion 1
nombre_completo = nombre +" "+ apellido 
print(nombre_completo)
# opcion 2
nombre = "Gabriel"
nombre_completo = nombre + apellido 
print(nombre_completo)
# Verificar  el tipo de Variable
print("La variable es un:")
print(type(a))
print("La variable nombre_ completo es un:")
print(type(nombre_completo))

















































































