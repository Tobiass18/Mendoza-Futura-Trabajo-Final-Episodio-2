# Vamos a utilizar la función TYPE para determinar el tipo de dato

#datos tipo string: es un copnjumto de caracteres 
print(type("hola"))
print("hola")

#datos tipo INTERGER: Es un número entero(numero redondo)
print(type(5))
print(5)
# ¿Qué pasa si coloco un numero entero entre comillas?
print(type("5"))
print("5")
# Dato tipo FLOAT: es un numero que tiene decimales, es decir, no es entero(o redondito)
print(type(5.6))
print(5.6)
