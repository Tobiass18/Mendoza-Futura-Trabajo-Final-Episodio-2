 # Suma "+"
a = float(input("ingresar un valor: "))
b = float(input("ingresar otro valor: ")) 
c = a + b 
#print("la suma es: " + str(c) )
print(f"la suma da como resultado: {c}")
# Resta "-"
c = a - b 
print(f"la resta da como resultado: {c}")
# Multiplicacion "*"
c = a * b 
print(f"la multiplicacion da como resulttado: {c}")
#division "/"
# en la division nunca se puede dividir por cero
c = a / b 
print(f"la division da como resulttado: {c}")
# potencia "**"
# Realiza la potencia entre los dos numeros	ejemplo: 12 ** 3 = 1728
c = a ** b 
print(f"la potencia da como resulttado: {c}")
# division entera
# en esta divison redondea al numero entero mas cercano 
#Realiza la división con resultado de número entero	18 // 5 = 3
c = a // b 
print(f"la division entera da como resulttado: {c}")
# modulo
c = a % b 
print(f"la modulo da como resulttado: {c}")