# dato tipo STRING: conjunto de caracteres 
# VAMOS A UTILIZAR LA FUNCION TYPE PARA DERTERMINAR EL TIPO DE DATO
print(type("hola"))
print("hola")

#dato tipo interger: es un numero entero (numero redondo)
print(type(5))
print(5)





# que pasa si coloco un numero entero entre comillas 
print(type("5"))
