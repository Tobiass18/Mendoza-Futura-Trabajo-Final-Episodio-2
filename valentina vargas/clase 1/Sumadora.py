# Realizaremos una calculadora que solo suma:

# Utilizaremos el int para convertir el dato ingresado a entero
# Le pedimos al usuario que ingrese un numero por consola
a = int(input("Ingresa un dato: "))
# Le pedimos al usuario que ingrese otro numero
b = int(input("ingresa otro numero: "))
# Realizamos la suma
c = a + b 
# Le mostramos el resultado al usuario 
print("el resultado de la suma es: " + str(c))
