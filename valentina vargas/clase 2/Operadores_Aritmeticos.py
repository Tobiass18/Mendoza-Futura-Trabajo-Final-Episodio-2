a = float(input("INGRESAR UN VALOR: "))
b = float(input("INGRESAR UN VALOR: "))
# SUMA "+"
c = a + b 
print(f"la suma da como resultado: {c}")

# RESTA "-"
c = a - b
print(f"la resta da como resultado: {c}")

# MULTIPLICACION "*"
c = a * b
print(f"la multiplicacion da como resultado: {c}")

# DIVISION "/"
c = a / b
print(f"la division da como resultado: {c}")

# POTENCIA "**"
c = a ** b
print(f"la potencia da como resultado: {c}")

# DIVISION ENTERA (redondea el N)
c = a // b
print(f"la division entera da como resultado: {c}")

# MODULO (resto de la division)
c = a % b
print(f"la % da como resultado: {c}")

